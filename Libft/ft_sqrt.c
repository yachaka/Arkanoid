int		ft_sqrt(int nb)
{
	int i;
	int to;
	int power;

	i = 1;
	to = nb / 2;
	while (i < to)
	{
		power = i * i;
		if (power > nb)
			return (0);
		if (power == nb)
			return (i);
		i++;
	}
	return (0);
}
